#include <iostream>///библиотера
void main()///функция
{
	double d, f, i = - 6, x = 4.5, z = 1.5*pow(10,-6);/// ввод переменных
	d = tan(-x * i) / sqrt(x - z);///формула для первого числа
	f = sin(2*d) / d;/// формула для второго числа
	std::cout << "d=" << d;/// вывод первого числа
	std::cout << "f=" << f;///вывод второго числа
}
